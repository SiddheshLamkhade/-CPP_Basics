#include<bits/stdc++.h>
using namespace std;

class Solution {
  public:
    string revStr(string S) {
        int totallength=S.length(); 
        int end=totallength-1;
        for(int i=end;i>=0;i--){
            cout<<S[i];}

        }};
        
        
int main(){
    Solution obj;
    string S;
    cin>>S;
    obj.revStr(S);
    return 0;
}